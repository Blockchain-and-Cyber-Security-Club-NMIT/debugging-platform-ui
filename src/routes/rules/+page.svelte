<div class="text-column">
	<h1>Rules - Coding Round</h1>

	<p>Please read the following rules carefully before participating in the Coding Round:</p>

	<ol>
		<li>
			<strong>Time Limit:</strong> Each coding question has a specific time limit mentioned in the question
			description. You must submit your solution within that time limit. Once the time is up, you will
			not be able to submit your code.
		</li>

		<li>
			<strong>Programming Languages:</strong> You can choose from the supported programming languages
			for each question. The available languages will be mentioned in the question description or the
			language dropdown.
		</li>

		<li>
			<strong>Code Editor:</strong> Use the provided code editor to write and test your code. You can
			switch between different programming languages using the language dropdown in the code editor.
		</li>

		<li>
			<strong>Testing:</strong> Before submitting your code, make sure to test it thoroughly. You can
			use the "Run" button in the code editor to execute your code and see the output. The output will
			be displayed in the "Output" section below the code editor.
		</li>

		<li>
			<strong>Submission:</strong> Once you are satisfied with your solution, click the "Submit" button
			to submit your code. You will not be able to modify your code after submission.
		</li>

		<li>
			<strong>Evaluation:</strong> Your submitted code will be evaluated based on correctness and efficiency.
			Make sure your code passes all the test cases and provides the expected output.
		</li>

		<li>
			<strong>Plagiarism:</strong> Plagiarism is strictly prohibited. Do not copy code from other participants
			or external sources. Any form of cheating or unfair means will lead to disqualification.
		</li>

		<li>
			<strong>Communication:</strong> You are not allowed to communicate with other participants during
			the coding round. If you have any questions or doubts, please reach out to the event organizers
			or the designated support team.
		</li>

		<li>
			<strong>Code of Conduct:</strong> Participants are expected to maintain a professional and respectful
			behavior throughout the coding round. Any form of harassment, discrimination, or unethical conduct
			will not be tolerated.
		</li>
	</ol>

	<p>
		By participating in the Coding Round, you agree to abide by these rules and the decisions of the
		event organizers. Failure to comply with the rules may result in disqualification from the
		round.
	</p>

	<p>Good luck and happy coding!</p>
</div>

